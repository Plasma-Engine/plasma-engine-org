try:
    import app
    print("âœ“ app module imported")
    import app.main
    print("âœ“ app.main imported")
    if os.path.exists("app/routers"):
        import app.routers
        print("âœ“ app.routers imported")
    print("\nâœ… All imports successful!")
except ImportError as e:
    print(f"\nâŒ Import failed: {e}")
    sys.exit(1)
